#triple pythagoras
m = float(input("masukkan nilai m ... "))
n = float(input("masukkan nilai n ... "))
sisi1 = m**2 - n**2
sisi2 = 2 * m * n
hipotenusa = m**2 + n**2
print("triple pythagoras dari nilai m dan n adalah: \nsisi 1 =", abs(sisi1), "\nsisi 2 =", abs(sisi2), "\nhipotenusa =", hipotenusa)
